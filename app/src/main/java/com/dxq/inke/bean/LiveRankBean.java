public class LiveRankBean {

    private int uid;
    private int dis_score;
    private String pic;
    private int score;
    private int level;

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getDis_score() {
        return dis_score;
    }

    public void setDis_score(int dis_score) {
        this.dis_score = dis_score;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }
}
